            Console.WriteLine(Math.Floor(3.59));
            Console.WriteLine(Math.PI);
            Console.WriteLine(Math.E);
            Console.WriteLine(Math.Sqrt(100));
            Console.WriteLine(Math.Pow(4, 3));
            Console.ReadKey();
        }
    }
}
